import { Inter, Rubik, Outfit, Albert_Sans } from "next/font/google";
import { Toaster } from "react-hot-toast";
import { NuqsAdapter } from "nuqs/adapters/next/app";
import { FooterProvider } from "@/context/footer-provider";
import localFont from "next/font/local";

import type { Metadata } from "next";

const rubik = Rubik({
  variable: "--font-rubik",
  fallback: ["sans-serif"],
});

const inter = Inter({
  variable: "--font-inter",
  fallback: ["sans-serif"],
});

const outfit = Outfit({
  variable: "--font-outfit",
  preload: true,
  fallback: ["sans-serif"],
  subsets: ["latin"],
});

const albertSans = Albert_Sans({
  variable: "--font-albert-sans",
  subsets: ["latin"],
  fallback: ["sans-serif"],
});

const jogging = localFont({
  src: "../../public/assets/fonts/Jogging.otf",
  variable: "--font-jogging",
  fallback: ["sans-serif"],
});

export const metadata: Metadata = {
  title: "Shop Meichu",
  description: "Generated by create next app",
};

export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  return (
    <html lang="en" suppressHydrationWarning>
      <body
        className={`${rubik.variable} ${inter.variable} ${outfit.variable} ${albertSans.variable} ${jogging.variable} antialiased`}
        suppressHydrationWarning
      >
        <NuqsAdapter>
            <FooterProvider>{children}</FooterProvider>
        </NuqsAdapter>
        <Toaster
          position="top-center"
          reverseOrder={false}
          containerClassName="font-outfit"
        />
      </body>
    </html>
  );
}
